package lab05;

/**
 * Class Pair to can make a list that has two values in its nodes. 
 * @author Juan Sebastián Pérez Salazar 
 * @version 1
 */
public class Pair
{
    public int vertex;
    public int weight;
    
    /**
     * Constructor of the class.
     * @param v vertex
     * @param p weight
     */
    public Pair(int v,int p){
        vertex = v; 
        weight = p;
    }
}
